package for문;

public class Ex01for문 {
	public static void main(String[] args) {
		
		// for문 :  반복 횟수가 정해져 있을 경우 사용한다
		// for(초기화 구문; 조건식; 반복 후 작업)
		// for(시작점; 도착점; 보폭)
		// 보폭 : 시작점에서 도착점까지 얼마만큼의 보폭으로 이동할 건지 정해준다
		// 각각의 조건은 ;(세미콜론)으로 구분
		
		// 1부터 3까지 출력하는 코드
		System.out.println(1);
		System.out.println(2);
		System.out.println(3);
		
		for (int i = 1; i < 4; i++) {
			System.out.println(i);
		}
		
		
		
	}

}
