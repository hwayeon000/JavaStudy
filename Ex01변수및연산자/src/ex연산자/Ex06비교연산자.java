package ex연산자;

public class Ex06비교연산자 {
	public static void main(String[] args) {
		
		// 비교연산자 
		// >, <, >=, <=, ==(같다), !=(다르다)
		// : 비교연산자는 결과값 무조건! boolean(논리형) 결과값을 돌려줌
		// true/false
		
		System.out.println(3 > 5);
		System.out.println(5 < 6);
		System.out.println(5 == 6);
		System.out.println(5 != 6);
		
		
	}

}
