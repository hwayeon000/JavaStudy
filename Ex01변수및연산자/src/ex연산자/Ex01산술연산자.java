package ex연산자;

public class Ex01산술연산자 {
	public static void main(String[] args) {
		
		// 산술연산자
		// +, -, *, /(몫), %(나머지)
		
		// 3과 4를 더한 결과값 출력
		System.out.println(3+4);
		
		// 15와 78 곱한 결과값
		System.out.println(15*78);
		
		System.out.println('집'+'가');
		System.out.println(7 + 3 + "7");
		// 7+3 ---> 10
		// 10 + "7" : 앞 혹은 뒤의 숫자(int) 자료형이 자동으로 문자열로 형변환
		System.out.println(7 + "3" + 7);
		// 컴파일 방향은 위에서 아래, 왼쪽에서 오른쪽
		// 7 + "3" -> "73"
		// "73" + 7 -> "737"
		System.out.println("7" + 3 + 7);
		// "7" + 3 -> "73"
		// "73" + 7 -> "737"
		System.out.println("조"+"자"+"연");
		// 문자 연산 +만 가능
		// 문자를 연결하는 기능
		
		
		
		
		
		
	}

}
