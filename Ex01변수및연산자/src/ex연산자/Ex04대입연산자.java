package ex연산자;

public class Ex04대입연산자 {
	public static void main(String[] args) {
		
		// 대입 연산자  ---> 이항 연산자
		// = : 값을 대입할 때 사용
		
		// 복합 대입 연산자
		// +=, -=, *=, /=, %=
		
		int num = 1;
		
		num += 2;  // num = num + 2;
		System.out.println(num); // 3
		
		num *= 5;  // num = num * 5
		System.out.println(num); // 15
		
		// 복합 대입 연산자는 보통 값을 "누적"시킬 때 많이 사용
		
		
		
		
	}

}
